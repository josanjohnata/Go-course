// Vari√°veis, valores & tipos
// Para declarar uma variavel se utiliza o simbolo da marmota :=
// para atribuir um novo valor a variavel se utiliza o simbolo =

package main

import (
	"fmt"
)

func main() {
	x := 10
	y := "Hello world!"
	a := true
// Para ver os valores das variaveis se usa o simbolo %v
// Para ver os tipos das variaveis se usa o simbolo %t
	fmt.Println("x: %v, %t\n, x, x, x")
	fmt.Println("y: %v, %t\n, y, y, y")
	fmt.Println("a: %v, %t, a, a, a")
}